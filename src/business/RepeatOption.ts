/**
 * Repeat options
 * Part of ProductUnit, range 10 - 39
 */
export enum RepeatOption {
    /**
     * Hour
     * 小时
     */
    HOUR = 10,

    /**
     * Day
     * 天
     */
    DAY = 11,

    /**
     * Year
     * 年
     */
    YEAR = 12,

    /**
     * Week
     * 周
     */
    WEEK = 21,

    /**
     * Two weeks
     * 两周
     */
    FORTNIGHT = 22,

    /**
     * Four weeks
     * 四周
     */
    FOURWEEK = 24,

    /**
     * Month
     * 月
     */
    MONTH = 31,

    /**
     * Two months
     * 两月
     */
    BIMONTH = 32,

    /**
     * Quater(3 months)
     * 季(三个月)
     */
    QUATER = 33,

    /**
     * Half a year
     * 半年
     */
    HALFYEAR = 36
}
